package JavaBasics;

public class percentageOf5Subjects {
    public static void main(String[] args) {
        double sub1 = 95;
        double sub2 = 86;
        double sub3 = 80;
        double sub4 = 88;
        double sub5 = 91;

        double per = (sub1 + sub2 + sub3 + sub4 + sub5) / 5;

        System.out.println("percentage is " + per);
    }
}
